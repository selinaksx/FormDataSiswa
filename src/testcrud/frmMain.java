    public void selectData() {
                String kolom[] = {"NIS","Nama","JenisKelamin","Angkatan","Kelas","Email","KotaAsal","Alamat"};
                DefaultTableModel dtm = new DefaultTableModel(null, kolom);
                String SQL ="SELECT * FROM t_siswa";
                ResultSet rs = KoneksiDB.executeQuery(SQL);
                try{
                    while(rs.next()){
                        String NIS = rs.getString(1);
                        String Nama = rs.getString(2);
                        String JenisKelamin = rs.getString(3);
                        String Angkatan = rs.getString(4);
                        if ("L".equals(rs.getString(3))){
                            JenisKelamin = "Laki-laki";
                        }else{
                            JenisKelamin = "Perempuan";
                        }
                        String Kelas = rs.getString(5);
                        String Email = rs.getString(6);
                        String AsalKota = rs.getString(7);
                        String Alamat = rs.getString(8);
                        String data[] = {NIS,Nama,JenisKelamin,Angkatan,Kelas,Email,AsalKota,Alamat};
                        dtm.addRow(data);
                    }
                    } catch (SQLException ex) {
                       Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE,null,ex);
                }
                tData.setModel(dtm);
    
    }
