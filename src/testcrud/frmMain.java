    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if("".equals(tfNIS.getText()) || "".equals(tfNama.getText()) || "".equals(tfAngkatan.getText()) || "".equals(taAlamat.getText()) || "".equals(tfEmail.getText()) || "".equals(jLabel20.getText()) || "".equals(tfKota.getText())) {
        JOptionPane.showMessageDialog(this, "Harap Lengkapi Data","Error", JOptionPane.WARNING_MESSAGE);
    } else {
        String JK = "";
        if (rbLk.isSelected()){
            JK = "L";
        }else{
            JK = "P";
        }
        
        String SQL = "INSERT INTO t_siswa (NIS,NamaSiswa,JenisKelamin,Angkatan,Kelas,Email,AsalKota,Alamat) VALUES ('"+tfNIS.getText()+"','"
                +tfNama.getText()+"','"+JK+"','"+tfAngkatan.getText()+"','"+tfKelas.getText()
                +"','"+tfEmail.getText()+"','"+tfKota.getText()+"','"+taAlamat.getText()+"')";
        int status = KoneksiDB.execute(SQL);
        if (status == 1) {
            JOptionPane.showMessageDialog(this, "Data berhasil ditambahkan","Sukses",JOptionPane.INFORMATION_MESSAGE);
            selectData();
        }else{
            JOptionPane.showMessageDialog(this, "Data gagal ditambahkan","Gagal",JOptionPane.WARNING_MESSAGE);
        }
    }   
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int baris = tData.getSelectedRow();
        if(baris != -1) {
            String NIS = tData.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM t_siswa WHERE NIS ='"+NIS+"'";
            int status = KoneksiDB.execute(SQL);
            if (status==1) {
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus", "Sukses", JOptionPane.INFORMATION_MESSAGE);
            }else{
                JOptionPane.showMessageDialog(this, "Data gagal dihapus","Gagal",JOptionPane.WARNING_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(this, "Pilih baris data terlebih dahulu","Error",JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

